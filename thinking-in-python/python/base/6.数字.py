# =================数字(Number)类型=================
"""
python中数字有四种类型：整数、布尔型、浮点数和复数。

int(整数)：如 1, 只有一种整数类型 int，表示为长整型，没有 python2 中的 Long。
bool(布尔)：如 True、False。
float(浮点数)：如 1.23、4.56
complex(复数)：如 1 + 2j、 1.1 + 2.2j

数据之间可以进行进制转换：
hex()：转换为十六进制
oct()：转换为八进制
bin()：转换为二进制
int()：转换为十进制
"""

# 十进制
num1 = 100
print("num1：", num1)

# 十六进制
num2 = 0Xff
print("num2：", num2)

# 八进制
num3 = 0o75
print("num3：", num3)

# 二进制
num4 = 0b101
print("num4：", num4)
